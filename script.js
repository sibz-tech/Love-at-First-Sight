// ========================
// Navigation Logic
// ========================
let currentFolderIndex = 0;
let currentView = "cover"; // cover, copyright, dedication, toc, folder-cover, folder-inside

function showPage(pageType) {
    const app = document.getElementById("app");
    let html = "";

    document.body.className = ""; // Clear classes on pages with background images
    if (pageType === "cover") {
        document.body.style.backgroundImage = `url('${coverPage.image}')`;
        html = `
            <h1>${coverPage.title}</h1>
            <button onclick="showPage('copyright')">${coverPage.button}</button>
        `;
    }

    else if (pageType === "copyright") {
        document.body.style.backgroundImage = `url('${copyrightPage.image}')`;
        html = `
            <p>${copyrightPage.text}</p>
            <button onclick="showPage('dedication')">${copyrightPage.button}</button>
        `;
    }

    else if (pageType === "dedication") {
        document.body.style.backgroundImage = `url('${dedicationPage.image}')`;
        html = `
            <p>${dedicationPage.text}</p>
            <button onclick="showPage('toc')">${dedicationPage.button}</button>
        `;
    }

    else if (pageType === "toc") {
        document.body.style.backgroundImage = `url('${tocPage.image}')`;
        html = `<h1>${tocPage.title}</h1>`;
        tocPage.folders.forEach(folder => {
            html += `<button onclick="openFolder(${folder.index})">${folder.title}</button>`;
        });
        html += `<button onclick="showPage('cover')">${tocPage.buttonHome}</button>`;
    }

    else if (pageType === "folder-cover") {
        const folder = folders[currentFolderIndex];
        document.body.style.backgroundImage = `url('${folder.cover.image}')`;
        html = `
            <h1>${folder.cover.title}</h1>
            <p>${folder.cover.subtitle}</p>
            <p>${folder.cover.song}</p>
            <button onclick="showPage('folder-inside')">Open My Heart</button>
            <button onclick="nextFolder()">Next Folder</button>
        `;
    }

   else if (pageType === "folder-inside") {
    const folder = folders[currentFolderIndex];
    document.body.style.backgroundImage = ""; // No image
    document.body.className = "inside-theme"; // CSS color theme
        html = `
            <h1>${folder.inside.heading}</h1>
            <p>${folder.inside.message}</p>
            <blockquote>${folder.inside.quote}</blockquote>
            <button onclick="showPage('toc')">Back to TOC</button>
            <button onclick="showPage('cover')">Home</button>
        `;
    }

    app.innerHTML = html;
    currentView = pageType;
}

function openFolder(index) {
    currentFolderIndex = index;
    showPage("folder-cover");
}

function nextFolder() {
    currentFolderIndex = (currentFolderIndex + 1) % folders.length;
    showPage("folder-cover");
}

// Start app
window.onload = () => {
    showPage("cover");
};
